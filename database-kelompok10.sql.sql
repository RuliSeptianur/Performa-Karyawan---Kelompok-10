-- ========================================================
-- DATABASE: PERANCANGAN DATA SBU (SEMUA TABEL RELASI > 100 DATA)
-- ========================================================
CREATE DATABASE IF NOT EXISTS Perancangan_Data_SBU;
USE Perancangan_Data_SBU;

SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS Performa_Review;
DROP TABLE IF EXISTS Indikator_Perilaku;
DROP TABLE IF EXISTS Soft_Kompetensi;
DROP TABLE IF EXISTS Hard_Kompetensi;
DROP TABLE IF EXISTS Kompetensi;
DROP TABLE IF EXISTS Karyawan;
DROP TABLE IF EXISTS Divisi;
DROP TABLE IF EXISTS SBU;
SET FOREIGN_KEY_CHECKS = 1;

-- STRUKTUR TABEL
CREATE TABLE SBU (
    ID_SBU VARCHAR(10) PRIMARY KEY,
    Nama_SBU VARCHAR(100) NOT NULL,
    Visi TEXT,
    Misi TEXT,
    Tujuan TEXT,
    Fokus_Pasar VARCHAR(50)
);

CREATE TABLE Divisi (
    ID_divisi VARCHAR(10) PRIMARY KEY,
    ID_SBU VARCHAR(10),
    Nama_Divisi VARCHAR(100) NOT NULL,
    FOREIGN KEY (ID_SBU) REFERENCES SBU(ID_SBU)
);

CREATE TABLE Karyawan (
    NIK VARCHAR(15) PRIMARY KEY,
    Nama_Karyawan VARCHAR(100) NOT NULL,
    Tgl_Masuk DATE,
    No_Telp VARCHAR(15),
    Jabatan VARCHAR(50),
    Email VARCHAR(100),
    Alamat TEXT,
    Nama_Atasan VARCHAR(100),
    ID_Divisi VARCHAR(10),
    FOREIGN KEY (ID_Divisi) REFERENCES Divisi(ID_divisi)
);

CREATE TABLE Kompetensi (
    Kode_Kompetensi VARCHAR(10) PRIMARY KEY,
    Nama_Kompetensi VARCHAR(100) NOT NULL,
    Jenis_Hard_Soft VARCHAR(10),
    Deskripsi TEXT
);

CREATE TABLE Hard_Kompetensi (
    Kode_Kompetensi VARCHAR(10) PRIMARY KEY,
    Skor_Kompetensi DECIMAL(4,2),
    FOREIGN KEY (Kode_Kompetensi) REFERENCES Kompetensi(Kode_Kompetensi)
);

CREATE TABLE Soft_Kompetensi (
    Level_Kompetensi VARCHAR(20),
    Kode_Kompetensi VARCHAR(10),
    Perilaku_Kunci TEXT,
    Kategori VARCHAR(50),
    Deskripsi TEXT,
    PRIMARY KEY (Level_Kompetensi, Kode_Kompetensi),
    FOREIGN KEY (Kode_Kompetensi) REFERENCES Kompetensi(Kode_Kompetensi)
);

CREATE TABLE Indikator_Perilaku (
    ID_Indikator VARCHAR(10) PRIMARY KEY,
    Deskripsi_Indikator TEXT,
    Level_Kompetensi VARCHAR(20)
);

CREATE TABLE Performa_Review (
    ID_Review VARCHAR(10) PRIMARY KEY,
    Tgl_Review DATE,
    Skor_Capaian DECIMAL(5,2),
    Periode VARCHAR(20),
    NIK VARCHAR(15),
    Kode_Kompetensi VARCHAR(10),
    FOREIGN KEY (NIK) REFERENCES Karyawan(NIK),
    FOREIGN KEY (Kode_Kompetensi) REFERENCES Kompetensi(Kode_Kompetensi)
);

